"""Write program to Add, Subtract, Multiply, Divide 2 Complex numbers."""

num1 = (1 + 2j)
num2 = (3 - 5j)
print("Addition of two complex numbers : ",num1+num2)
print("Subtraction of two complex numbers : ",num1-num2)
print("Multiplication of two complex numbers : ",num1*num2) # logic: (ac-bd) + (ad+bc)
print("Division of two complex numbers : ",num1/num2)


